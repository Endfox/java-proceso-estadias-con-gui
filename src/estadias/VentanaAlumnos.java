    private void fillEmpresas(){
        Empresa emp = null;
        ObjectSet resultado = this.dbempresa.queryByExample(emp);
	while(resultado.hasNext()){
           Empresa empl = (Empresa) resultado.next();
           this.jCEmpresas.addItem(empl.getNombre());
	}
    }
    
    private void fillCarreras(){
        this.carreras.forEach((carrera) -> this.jCCarrera.addItem(carrera));
    }
    
    private Empresa buscarObjetoEmpresa(String nombre_empresa){
        try{
            Empresa em = null;
            ObjectSet resultado = this.dbempresa.queryByExample(em);
            while(resultado.hasNext()){
               em = (Empresa) resultado.next();
               if(em.getNombre().equals(nombre_empresa)){
                   return em ;
               }
            }
            return null;
        }
        catch(DatabaseClosedException | Db4oIOException e){
            System.out.println(e.getMessage());
            return null;
        }
    }
    
