    private void jBguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBguardarActionPerformed
        String nombre = this.jTNombre.getText();
        String apellidos = this.jTApellidos.getText();
        String matricula = this.jTMatricula.getText();
        String carrera = (String) this.jCCarrera.getSelectedItem();
        String especialidad = (String) this.jCEspecialidades.getSelectedItem();
        String nombre_empresa = (String) this.jCEmpresas.getSelectedItem();
        Empresa empresa = this.buscarObjetoEmpresa(nombre_empresa);
        
        if(!matricula.isEmpty() && !nombre.isEmpty() && !apellidos.isEmpty() && !carrera.isEmpty() && !empresa.getNombre().isEmpty()){
            Alumno al = new Alumno(nombre,
                apellidos,
                carrera,
                especialidad,
                matricula,
                empresa);
            this.dbalumno.store(al);
            this.limpiarJText();
            this.jLStatus.setForeground(Color.BLUE);
            this.jLStatus.setText("Alumno ingresado correctamente");
        }
        else{
            this.jLStatus.setForeground(Color.RED);
            this.jLStatus.setText("El alumno no se ha ingresado correctamente");
        }
        
    }//GEN-LAST:event_jBguardarActionPerformed

